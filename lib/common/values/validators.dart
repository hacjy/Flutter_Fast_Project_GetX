RegExp userNameReg = RegExp(r"^[a-zA-Z\-\s_]{1,50}$");

//邮箱
bool isEmail(String str) {
  return emailReg.hasMatch(str.toLowerCase());
}

//full name
bool isUserName(String str) {
  return userNameReg.hasMatch(str.toLowerCase());
}

